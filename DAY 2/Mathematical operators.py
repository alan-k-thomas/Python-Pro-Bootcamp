#Priority of doing the operations in the same line of code in python is "PEMDAS"
# 1.() 
# 2.**
# 3.* or /
# 4.+ or -

print(3 * 3 + 3 / 3 - 3) #Answer is 7
print(3 * (3 + 3) / 3 - 3) #Brackets(Parenthesis) will be the first priority and the answer will be 3
